    private void resetAll() {
        brain = new TicTacToeBrain();
        for(int i = 0; i < panelArray.length; i++) {
            TicTacToeUnitPanel unitPanel = (TicTacToeUnitPanel)panelArray[i];
            unitPanel.reset();
            unitPanel.removeMouseListener(this);
            unitPanel.addMouseListener(this);
        }
        this.repaint();
    }
    
