    @Override
    public void mouseClicked(MouseEvent e) {
       TicTacToeUnitPanel source = (TicTacToeUnitPanel) e.getSource();
       source.removeMouseListener(this);
       String name = source.getName();
       int index = Integer.valueOf(name.split("_")[1]);
       int player = brain.fillTicTacToeAt(index);
       if(player == brain.playerCircle) {
           System.out.println("Circle is Pressed");
           source.drawType = DrawType.TypeCircle;
           source.repaint();
       } else if (player == brain.playerCross) {
           System.out.println("Cross is Pressed");
           source.drawType = DrawType.TypeCross;
           source.repaint();
       }
       
       int winner = brain.checkWinner();    
       if(winner != 0) {
           String winnerName = (winner == brain.playerCircle)?"Circle":"Cross";
           JOptionPane.showMessageDialog(this, winnerName + " won the game" );
           resetAll();
       }
    }
     
